const UTF8_DECODER = new TextDecoder('utf-8')
const MOJIBAKE_PAIR_RE = /[РСÐÑ][^\s]/g
const MOJIBAKE_SIGNAL_RE = /(Рџ|Рљ|РЎ|Рћ|вЂ|Г—|С‡|Р°|Рё)/g

function cp1251CharToByte(ch) {
  const code = ch.charCodeAt(0)
  if (code <= 0x7f) return code
  if (code <= 0xff) return code
  if (code >= 0x0410 && code <= 0x044f) return code - 0x350
  if (Object.prototype.hasOwnProperty.call(CP1251_SPECIAL_CHAR_TO_BYTE, ch)) {
    return CP1251_SPECIAL_CHAR_TO_BYTE[ch]
  }
  return null
}

function countMojibakePairs(text) {
  return (text.match(MOJIBAKE_PAIR_RE) || []).length
}

function looksLikeMojibake(text) {
  const compact = String(text || '').replace(/\s+/g, '')
  if (compact.length < 6) return false
  const pairs = countMojibakePairs(compact)
  if (pairs >= 2 && (pairs * 2) / compact.length >= 0.24) return true
  const signals = (compact.match(MOJIBAKE_SIGNAL_RE) || []).length
  return signals >= 3 && signals / compact.length >= 0.18
}

function decodeCp1251Utf8Mojibake(text) {
  const bytes = new Uint8Array(text.length)
  let i = 0
  for (const ch of text) {
    const byte = cp1251CharToByte(ch)
    if (byte === null) return text
    bytes[i++] = byte
  }
  const payload = i === bytes.length ? bytes : bytes.slice(0, i)
  return UTF8_DECODER.decode(payload)
}

function normalizeLegacyText(input) {
  const cleaned = replaceByMap(input, TOKEN_MAP_CLEANUP)
  if (!looksLikeMojibake(cleaned)) return cleaned
  const decoded = decodeCp1251Utf8Mojibake(cleaned)
  if (!decoded || decoded === cleaned) return cleaned
  return countMojibakePairs(decoded) < countMojibakePairs(cleaned) ? decoded : cleaned
}

const initial = safeStorageGet(STORAGE_KEY)
const locale = ref(SUPPORTED.includes(initial) ? initial : DEFAULT_LOCALE)

if (typeof document !== 'undefined') {
  document.documentElement.lang = locale.value
}

function setLocale(nextLocale) {
  if (!SUPPORTED.includes(nextLocale)) return
  locale.value = nextLocale
  safeStorageSet(STORAGE_KEY, nextLocale)
  if (typeof document !== 'undefined') {
    document.documentElement.lang = nextLocale
  }
}

function t(key, params = {}) {
  const current = resolvePath(messages[locale.value], key)
  const fallback = resolvePath(messages[DEFAULT_LOCALE], key)
  const raw = current ?? fallback ?? key
  return interpolate(raw, params)
}

function translateLegacyText(input, targetLocale) {
  if (!input || !targetLocale) return input

  const normalizedInput = normalizeLegacyText(input)
  const trimmed = normalizedInput.trim()
  if (!trimmed) return input

  let out = normalizedInput
  if (targetLocale === 'ru') out = replaceByMap(out, PHRASE_TO_RU)
  if (targetLocale === 'en') out = replaceByMap(out, PHRASE_TO_EN)
  if (targetLocale === 'uz') out = replaceByMap(out, PHRASE_TO_UZ)

  const phrase = PHRASE_MAP[trimmed]
  if (phrase && phrase[targetLocale]) {
    return out.replace(trimmed, phrase[targetLocale])
  }
  if (phrase && targetLocale === 'uz' && phrase.ru) {
    const uzFromRu = replaceByMap(phrase.ru, TOKEN_MAP_RU_TO_UZ)
    return out.replace(trimmed, uzFromRu)
  }

  if (targetLocale === 'ru') {
    return replaceByMap(replaceByMap(out, TOKEN_MAP_UZ_TO_RU), TOKEN_MAP_EN_TO_RU)
  }

  if (targetLocale === 'en') {
    return replaceByMap(replaceByMap(out, TOKEN_MAP_UZ_TO_EN), TOKEN_MAP_RU_TO_EN)
  }

  if (targetLocale === 'uz') {
    const firstPass = replaceByMap(replaceByMap(out, TOKEN_MAP_RU_TO_UZ), TOKEN_MAP_EN_TO_UZ)
    return replaceByMap(replaceByMap(firstPass, TOKEN_MAP_RU_TO_UZ), TOKEN_MAP_EN_TO_UZ)
  }

  return out
}

const textOriginalMap = new WeakMap()
const attrOriginalMap = new WeakMap()
let observer = null
let started = false
let rafId = null

function shouldSkipTextNode(node) {
  const parent = node.parentElement
  if (!parent) return true
  const tag = parent.tagName
  if (tag === 'SCRIPT' || tag === 'STYLE' || tag === 'NOSCRIPT' || tag === 'PRE' || tag === 'CODE') return true
  if (parent.closest('[data-no-auto-i18n]')) return true
  return false
}

function translateTextNode(node) {
  if (shouldSkipTextNode(node)) return
  const current = node.nodeValue
  if (!current || !current.trim()) return

  const original = textOriginalMap.get(node) ?? current
  if (!textOriginalMap.has(node)) textOriginalMap.set(node, original)

  const translated = translateLegacyText(original, locale.value)
  if (translated !== current) node.nodeValue = translated
}

function translateElementAttrs(el) {
  const attrs = ['placeholder', 'title', 'aria-label']
  let originalStore = attrOriginalMap.get(el)
  if (!originalStore) {
    originalStore = {}
    attrOriginalMap.set(el, originalStore)
  }

  for (const attr of attrs) {
    const current = el.getAttribute(attr)
    if (!current) continue
    if (originalStore[attr] === undefined) originalStore[attr] = current

    const translated = translateLegacyText(originalStore[attr], locale.value)
    if (translated !== current) el.setAttribute(attr, translated)
  }
}

function applyDomTranslations(root = document.body) {
  if (typeof document === 'undefined' || !root) return

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT)
  while (walker.nextNode()) translateTextNode(walker.currentNode)

  if (root.nodeType === 1) translateElementAttrs(root)
  const elements = root.querySelectorAll ? root.querySelectorAll('[placeholder],[title],[aria-label]') : []
  elements.forEach((el) => translateElementAttrs(el))
}

function scheduleDomTranslate() {
  if (typeof window === 'undefined') return
  if (rafId !== null) return
  rafId = window.requestAnimationFrame(() => {
    rafId = null
    applyDomTranslations(document.body)
  })
}

function startDomTranslation() {
  if (started || typeof window === 'undefined') return
  started = true

  scheduleDomTranslate()
  watch(locale, () => scheduleDomTranslate())

  observer = new MutationObserver(() => scheduleDomTranslate())
  observer.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ['placeholder', 'title', 'aria-label'],
  })
}

export function useI18n() {
  return {
    locale: computed(() => locale.value),
    setLocale,
    t,
    supportedLocales: SUPPORTED,
  }
}

export const i18n = {
  locale,
  setLocale,
  t,
  supportedLocales: SUPPORTED,
  startDomTranslation,
}
